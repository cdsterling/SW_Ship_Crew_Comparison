function fetchShipInfoAPI(){
    console.log("Entering fetchShipInfoAPI function");
    //before fetching anything, we have to make sure to update the indicies and the selected feature based on user input
    updateIndiciesAndSelectedFeature();
    let numberOfRequests = apiShipIndices.length;
    displayShips = []
    for (shipID of apiShipIndices){
        console.log("loading Index:",shipID);
        console.log("Total Number of Requests: ", numberOfRequests);
        fetch(`https://swapi.co/api/starships/${shipID}/`)
        .then(response => response.json())
        .then(data => {
            console.log("storing data for ship: ", data.name);
            console.log(data)
            displayShips.push(data);

            console.log('data[selectedFeature], for feature', selectedFeature, ": ", data[selectedFeature] );
            if (Number(data[selectedFeature]) > maxFeatureSize){
                maxFeatureSize = Number(data[selectedFeature]);
            }
            numberOfRequests--;
        });

    }
   
    console.log("Max Feature Size: " + maxFeatureSize);
    // displayShipInfoAPI();
    displayShipInfoAPI_innerHTML();
}


