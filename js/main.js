function displayMenuItems(){
    //display Dropdown of feature selections
    let dropDown = document.querySelector('.BarChart-select');
    for (let feature in SW_ShipFeatures){
        let myOpt = document.createElement('option');
        myOpt.setAttribute("value", feature);
        let myText = document.createTextNode(SW_ShipFeatures[feature]);
        myOpt.appendChild(myText);
        if (feature === selectedFeature){
            myOpt.selected = true;
        }
        dropDown.appendChild(myOpt);
    }
    //display checkbox list of ships
    let shipListDiv = document.querySelector('.BarChart-shipSelection');
    for(let myShip in SW_Ships){
        let shipLabel=document.createElement('label');
        let shipInput=document.createElement('input');

        shipInput.type = "checkbox"; 
        shipInput.name = myShip; 
        shipInput.value = SW_Ships[myShip].index; 
        shipInput.id = myShip;
        shipInput.onclick = () =>{
            fetchShipInfoAPI();
        };
        shipInput.classList.add('BarChart-shipCheckbox');
        if(apiShipIndices.includes(SW_Ships[myShip].index)){
            shipInput.checked = true;
        }
        
        shipLabel.htmlFor= myShip;
        console.log("SW_Ships[myShip]:", SW_Ships[myShip].name);
        shipLabel.innerText = SW_Ships[myShip].name;

        console.log("label: ", shipLabel);
        console.log("shipInput: ", shipInput)
        shipLabel.appendChild(shipInput);
        shipListDiv.appendChild(shipLabel);

    }
}

function updateIndiciesAndSelectedFeature(){
    console.log("entering updateIndiciesAndSelectedFeature function")
    let featureDropdown = document.querySelector('.BarChart-select');
    let shipCheckboxes = document.querySelectorAll('.BarChart-shipCheckbox');

    // update selectedFeature
    console.log("featureDropdown: ", featureDropdown);
    console.log("featureDropdown.options", featureDropdown.options);
    console.log("featureDropdown.selectedIndex", featureDropdown.selectedIndex);
    console.log("featureDropdown.options[featureDropdown.selectedIndex]", featureDropdown.options[featureDropdown.selectedIndex]);
    selectedFeature = featureDropdown.options[featureDropdown.selectedIndex].value;

    // update apiShipIndices
    apiShipIndices = []
    console.log("shipCheckboxes:", shipCheckboxes);
    for(myCheckBox of shipCheckboxes){
        //console.log("myCheckbox:", myCheckBox)
        if(myCheckBox.checked){
            apiShipIndices.push(myCheckBox.value)
        }
    }
    console.log("updated selectedFeature: ", selectedFeature);
    console.log("updated ship index list", apiShipIndices);

}


